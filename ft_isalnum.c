#include "libft.h"

int	ft_isalnum(int i)
{
	if (!(i >= '0' && i <= '9' ))
	{
		if (!(i >= 'A' && i <= 'Z'))
		{
			if (!(i >= 'a' && i <= 'z'))
				return (0);
		}
	}
	return (1);
}
